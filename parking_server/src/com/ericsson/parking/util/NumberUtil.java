public class NumberUtil {
	
	public static Integer parseInt(Object obj) {
		if (obj == null)
			return null;
		String str = obj.toString();
		try {
			Integer integer = Integer.parseInt(str);
			return integer;
		} catch (Exception e) {
			return null;
		}
	}
}
